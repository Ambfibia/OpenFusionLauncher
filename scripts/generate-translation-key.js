const fs = require('fs');
const path = require('path');

const localeFile = path.join(__dirname, '..', 'resources', 'locales', 'en.json');
const outFile = path.join(__dirname, '..', 'app', 'translation-key.ts');

const data = JSON.parse(fs.readFileSync(localeFile, 'utf8'));
const keys = Object.keys(data).sort();

const content = `// This file is auto-generated by scripts/generate-translation-key.js\n` +
  `// Do not edit manually.\n` +
  `export type TranslationKey =\n` +
  keys.map((k) => `  | '${k}'`).join('\n') +
  `;\n`;

fs.writeFileSync(outFile, content);
